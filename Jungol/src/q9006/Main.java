package q9006;

public class Main {

	public static void main(String[] args) {
		String a = "수를 출력할 때는 따옴표를 생략합니다.";
		int b = 123;
		float c = 6.5f;
		System.out.println(a);
		System.out.println(b);
		System.out.printf("%.6f\n", c);
		/*
		 * %d : 정수
		 * %f : 실수
		 * %s : 문자열
		 * %c : 문자
		 */
	}

}